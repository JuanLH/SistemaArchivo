    public final void setNextId(){
        ArrayList<Transferencia> list;
        try {
            list = transferencia.getLista(file);
            if(!list.isEmpty())
                txtId.setText(Integer.toString(list.get(list.size()-1).getId_transferencia()+1));
            else
               txtId.setText("1");
            
        } catch (IOException ex) {
            System.err.println("Error leyendo el archivo xxxxxxxxxxxxxxxxxx");
        }
        catch (IndexOutOfBoundsException ex){
            System.err.println("index incorrecto xxxxxxxxxxxxxxxxxxxxx");
        }
    }
    
    public void consultTransferencia(){
        DefaultTableModel modelo = new DefaultTableModel();
        
        String [] cols = {"id","id_cuenta_origen","id_cuenta_destino","id_persona","monto","time","comentario"};
        for (int i=0;i<cols.length;i++)
            modelo.addColumn(cols[i]);
        
        int k;
        ArrayList<Transferencia> list;
        try {
            list = transferencia.getLista(file);
            
            for(Transferencia t : list){
                k=0;
                Object[] fila = new Object[7];
                fila[k++]=(Object)t.getId_transferencia();
                fila[k++]=(Object)t.getId_cuenta_origen();
                fila[k++]=(Object)t.getId_cuenta_destino();
                fila[k++]=(Object)t.getId_persona();
                fila[k++]=(Object)t.getMonto();
                fila[k++]=(Object)t.getTime();
                fila[k++]=(Object)t.getComentario();
                modelo.addRow(fila);
            }
            
        } catch (IOException ex) {
            System.err.println("Error al consultar Persona");
        }
        tableTransferencia.setModel(modelo);
    }
    
